// 经纬度 → 画布百分比坐标
export function lngLatToPercent(lng, lat) {
  const { west, east, north, south } = HZ_BOUNDS
  return {
    x: ((lng - west) / (east - west)) * 100,
    y: ((north - lat) / (north - south)) * 100
  }
}

