// 杭州范围常量
export const HZ_BOUNDS = {
  west: 118.21,
  east: 120.50,
  north: 30.55,
  south: 29.11
}

// 经纬度 → 画布百分比(0-100),在 mock 里直接用
function pct(lng, lat) {
  const { west, east, north, south } = HZ_BOUNDS
  return {
    x: Math.round(((lng - west) / (east - west)) * 1000) / 10,
    y: Math.round(((north - lat) / (north - south)) * 1000) / 10
  }
}

// 工具:从一个对象生成带 position 的 spot
function spot(fields) {
  const p = pct(fields.coords.lng, fields.coords.lat)
  return { ...fields, position: { x: p.x, y: p.y } }
}

